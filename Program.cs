Console.WriteLine("Hello, World!");
using System;

class БанківськийРахунок
{
    private string НомерРахунку;
    private double Баланс;

    // Конструктор класу для ініціалізації номеру рахунку та початкового балансу
    public БанківськийРахунок(string номерРахунку, double початковийБаланс)
    {
        НомерРахунку = номерРахунку;
        Баланс = початковийБаланс;
    }

    // Метод для поповнення балансу рахунку на задану суму
    public void ПоповнитиРахунок(double сума)
    {
        if (сума > 0)
        {
            Баланс += сума;
            Console.WriteLine($"Рахунок {НомерРахунку} поповнено на {сума} грн. Новий баланс: {Баланс} грн.");
        }
        else
        {
            Console.WriteLine("Сума для поповнення повинна бути більше нуля.");
        }
    }

    // Метод для зняття грошей з рахунку
    public void ЗнятиГроші(double сума)
    {
        if (сума > 0)
        {
            if (Баланс >= сума)
            {
                Баланс -= сума;
                Console.WriteLine($"З рахунку {НомерРахунку} знято {сума} грн. Новий баланс: {Баланс} грн.");
            }
            else
            {
                Console.WriteLine($"На рахунку недостатньо коштів для зняття {сума} грн.");
            }
        }
        else
        {
            Console.WriteLine("Сума для зняття повинна бути більше нуля.");
        }
    }

    // Метод для виведення інформації про рахунок
    public void ПоказатиІнформацію()
    {
        Console.WriteLine($"Номер рахунку: {НомерРахунку}");
        Console.WriteLine($"Поточний баланс: {Баланс} грн.");
    }
}

class Program
{
    static void Main()
    {
        // Створення об'єкта "Банківський рахунок" з початковим балансом 1000 грн
        БанківськийРахунок рахунок1 = new БанківськийРахунок("1234567890", 1000);

        // Виведення інформації про рахунок
        рахунок1.ПоказатиІнформацію();

        // Поповнення рахунку на 500 грн
        рахунок1.ПоповнитиРахунок(500);

        // Зняття 200 грн з рахунку
        рахунок1.ЗнятиГроші(200);

        // Спроба зняти більше коштів, ніж є на рахунку
        рахунок1.ЗнятиГроші(1000);

        // Виведення оновленої інформації про рахунок
        рахунок1.ПоказатиІнформацію();
    }
}
